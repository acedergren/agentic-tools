  PROCEDURE enable_audit_policies IS
    v_policy_exists NUMBER;

    TYPE t_policies IS TABLE OF VARCHAR2(100);
    v_builtin_policies t_policies := t_policies(
      'ORA_LOGON_FAILURES',
      'ORA_SECURECONFIG',
      'ORA_DBA_POLICY'
    );
  BEGIN
    DBMS_OUTPUT.PUT_LINE(CHR(10) || '=== PHASE 5: Enabling Audit Policies ===' || CHR(10));

    -- Enable built-in policies
    FOR i IN 1..v_builtin_policies.COUNT LOOP
      SELECT COUNT(*) INTO v_policy_exists
      FROM audit_unified_enabled_policies
      WHERE policy_name = v_builtin_policies(i);

      IF v_policy_exists = 0 THEN
        g_issues_found := g_issues_found + 1;

        IF NOT g_dry_run THEN
          BEGIN
            EXECUTE IMMEDIATE 'AUDIT POLICY ' || v_builtin_policies(i);
            g_issues_fixed := g_issues_fixed + 1;
            log_action('AUDITING', 'ENABLE_POLICY', v_builtin_policies(i), 'Built-in policy enabled', 'FIXED');
          EXCEPTION
            WHEN OTHERS THEN
              log_action('AUDITING', 'ENABLE_POLICY', v_builtin_policies(i), NULL, 'ERROR', SQLERRM);
          END;
        ELSE
          log_action('AUDITING', 'ENABLE_POLICY', v_builtin_policies(i), 'Built-in policy', 'WOULD_FIX');
        END IF;
      ELSE
        log_action('AUDITING', 'CHECK_POLICY', v_builtin_policies(i), 'Already enabled', 'OK');
      END IF;
    END LOOP;

    -- Create custom security audit policy
    SELECT COUNT(*) INTO v_policy_exists
    FROM audit_unified_policies
    WHERE policy_name = 'SECURITY_REMEDIATION_POLICY';

    IF v_policy_exists = 0 THEN
      g_issues_found := g_issues_found + 1;

      IF NOT g_dry_run THEN
        BEGIN
          EXECUTE IMMEDIATE '
            CREATE AUDIT POLICY security_remediation_policy
              ACTIONS
                ALTER SYSTEM, ALTER DATABASE,
                CREATE USER, ALTER USER, DROP USER,
                GRANT, REVOKE,
                CREATE ROLE, ALTER ROLE, DROP ROLE,
                CREATE DIRECTORY, DROP DIRECTORY';
          EXECUTE IMMEDIATE 'AUDIT POLICY security_remediation_policy';
          g_issues_fixed := g_issues_fixed + 1;
          log_action('AUDITING', 'CREATE_POLICY', 'SECURITY_REMEDIATION_POLICY',
                     'Custom security policy created and enabled', 'FIXED');
        EXCEPTION
          WHEN OTHERS THEN
            log_action('AUDITING', 'CREATE_POLICY', 'SECURITY_REMEDIATION_POLICY', NULL, 'ERROR', SQLERRM);
        END;
      ELSE
        log_action('AUDITING', 'CREATE_POLICY', 'SECURITY_REMEDIATION_POLICY',
                   'Would create custom policy', 'WOULD_FIX');
      END IF;
    ELSE
      log_action('AUDITING', 'CHECK_POLICY', 'SECURITY_REMEDIATION_POLICY', 'Already exists', 'OK');
    END IF;
  END enable_audit_policies;

