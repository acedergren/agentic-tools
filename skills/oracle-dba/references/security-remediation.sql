  -- ===========================================================================
  -- 1. REVOKE DANGEROUS PRIVILEGES
  -- ===========================================================================
  PROCEDURE revoke_dangerous_privileges IS
    CURSOR c_privs IS
      SELECT grantee, privilege
      FROM dba_sys_privs
      WHERE privilege IN (
        'DBA', 'ALTER SYSTEM', 'DROP ANY TABLE', 'ALTER ANY TABLE',
        'DELETE ANY TABLE', 'INSERT ANY TABLE', 'UPDATE ANY TABLE',
        'CREATE ANY DIRECTORY', 'GRANT ANY PRIVILEGE', 'GRANT ANY ROLE',
        'ALTER DATABASE', 'CREATE ANY TRIGGER', 'EXECUTE ANY PROCEDURE',
        'SELECT ANY TABLE', 'CREATE ANY TABLE'
      )
      ORDER BY grantee, privilege;

    v_sql VARCHAR2(500);
  BEGIN
    DBMS_OUTPUT.PUT_LINE(CHR(10) || '=== PHASE 1: Revoking Dangerous Privileges ===' || CHR(10));

    FOR r IN c_privs LOOP
      IF NOT is_excluded_user(r.grantee) THEN
        g_issues_found := g_issues_found + 1;
        v_sql := 'REVOKE ' || r.privilege || ' FROM ' || r.grantee;

        IF NOT g_dry_run THEN
          BEGIN
            EXECUTE IMMEDIATE v_sql;
            g_issues_fixed := g_issues_fixed + 1;
            log_action('PRIVILEGES', 'REVOKE', r.grantee, r.privilege, 'FIXED');
          EXCEPTION
            WHEN OTHERS THEN
              log_action('PRIVILEGES', 'REVOKE', r.grantee, r.privilege, 'ERROR', SQLERRM);
          END;
        ELSE
          log_action('PRIVILEGES', 'REVOKE', r.grantee, r.privilege, 'WOULD_FIX');
        END IF;
      END IF;
    END LOOP;
  END revoke_dangerous_privileges;

  -- ===========================================================================
  -- 2. REVOKE PUBLIC GRANTS
  -- ===========================================================================
  PROCEDURE revoke_public_grants IS
    CURSOR c_public IS
      SELECT owner, table_name, privilege
      FROM dba_tab_privs
      WHERE grantee = 'PUBLIC'
        AND owner NOT IN ('SYS', 'SYSTEM', 'XDB', 'CTXSYS', 'MDSYS',
                          'ORDSYS', 'WMSYS', 'ORDDATA', 'LBACSYS')
        AND privilege IN ('SELECT', 'INSERT', 'UPDATE', 'DELETE', 'EXECUTE')
      ORDER BY owner, table_name;

    v_sql VARCHAR2(500);
  BEGIN
    DBMS_OUTPUT.PUT_LINE(CHR(10) || '=== PHASE 2: Revoking PUBLIC Grants ===' || CHR(10));

    FOR r IN c_public LOOP
      g_issues_found := g_issues_found + 1;
      v_sql := 'REVOKE ' || r.privilege || ' ON ' ||
               r.owner || '.' || r.table_name || ' FROM PUBLIC';

      IF NOT g_dry_run THEN
        BEGIN
          EXECUTE IMMEDIATE v_sql;
          g_issues_fixed := g_issues_fixed + 1;
          log_action('PUBLIC_GRANTS', 'REVOKE', r.owner || '.' || r.table_name, r.privilege, 'FIXED');
        EXCEPTION
          WHEN OTHERS THEN
            log_action('PUBLIC_GRANTS', 'REVOKE', r.owner || '.' || r.table_name, r.privilege, 'ERROR', SQLERRM);
        END;
      ELSE
        log_action('PUBLIC_GRANTS', 'REVOKE', r.owner || '.' || r.table_name, r.privilege, 'WOULD_FIX');
      END IF;
    END LOOP;
  END revoke_public_grants;

  -- ===========================================================================
  -- 3. LOCK INACTIVE ACCOUNTS
  -- ===========================================================================
  PROCEDURE lock_inactive_accounts IS
    CURSOR c_inactive IS
      SELECT u.username, u.last_login, u.account_status
      FROM dba_users u
      WHERE u.account_status = 'OPEN'
        AND u.username NOT IN (SELECT TRIM(REGEXP_SUBSTR(g_exclude_users, '[^,]+', 1, LEVEL))
                               FROM dual
                               CONNECT BY REGEXP_SUBSTR(g_exclude_users, '[^,]+', 1, LEVEL) IS NOT NULL)
        AND (u.last_login IS NULL OR u.last_login < SYSDATE - &INACTIVE_DAYS)
        AND NOT EXISTS (
          SELECT 1 FROM unified_audit_trail a
          WHERE a.dbusername = u.username
            AND a.event_timestamp > SYSDATE - &INACTIVE_DAYS
        )
      ORDER BY u.username;

    v_sql VARCHAR2(200);
  BEGIN
    DBMS_OUTPUT.PUT_LINE(CHR(10) || '=== PHASE 3: Locking Inactive Accounts (>' || &INACTIVE_DAYS || ' days) ===' || CHR(10));

    FOR r IN c_inactive LOOP
      g_issues_found := g_issues_found + 1;
      v_sql := 'ALTER USER ' || r.username || ' ACCOUNT LOCK';

      IF NOT g_dry_run THEN
        BEGIN
          EXECUTE IMMEDIATE v_sql;
          g_issues_fixed := g_issues_fixed + 1;
          log_action('ACCOUNTS', 'LOCK', r.username,
                     'Last login: ' || NVL(TO_CHAR(r.last_login, 'YYYY-MM-DD'), 'NEVER'), 'FIXED');
        EXCEPTION
          WHEN OTHERS THEN
            log_action('ACCOUNTS', 'LOCK', r.username, NULL, 'ERROR', SQLERRM);
        END;
      ELSE
        log_action('ACCOUNTS', 'LOCK', r.username,
                   'Last login: ' || NVL(TO_CHAR(r.last_login, 'YYYY-MM-DD'), 'NEVER'), 'WOULD_FIX');
      END IF;
    END LOOP;
  END lock_inactive_accounts;

  -- ===========================================================================
  -- 4. ENFORCE PASSWORD POLICY
  -- ===========================================================================
  PROCEDURE enforce_password_policy IS
    v_profile_exists NUMBER;
    v_sql VARCHAR2(1000);
  BEGIN
    DBMS_OUTPUT.PUT_LINE(CHR(10) || '=== PHASE 4: Enforcing Password Policy ===' || CHR(10));

    -- Check if secure profile exists
    SELECT COUNT(*) INTO v_profile_exists
    FROM dba_profiles WHERE profile = 'SECURE_APP_PROFILE' AND ROWNUM = 1;

    IF v_profile_exists = 0 THEN
      g_issues_found := g_issues_found + 1;

      IF NOT g_dry_run THEN
        BEGIN
          EXECUTE IMMEDIATE '
            CREATE PROFILE secure_app_profile LIMIT
              PASSWORD_LIFE_TIME 90
              PASSWORD_REUSE_TIME 365
              PASSWORD_REUSE_MAX 12
              PASSWORD_VERIFY_FUNCTION ora12c_strong_verify_function
              FAILED_LOGIN_ATTEMPTS 5
              PASSWORD_LOCK_TIME UNLIMITED
              PASSWORD_GRACE_TIME 7';
          g_issues_fixed := g_issues_fixed + 1;
          log_action('PASSWORD', 'CREATE_PROFILE', 'SECURE_APP_PROFILE',
                     '90-day expiry, 5 failed attempts lock', 'FIXED');
        EXCEPTION
          WHEN OTHERS THEN
            log_action('PASSWORD', 'CREATE_PROFILE', 'SECURE_APP_PROFILE', NULL, 'ERROR', SQLERRM);
        END;
      ELSE
        log_action('PASSWORD', 'CREATE_PROFILE', 'SECURE_APP_PROFILE',
                   '90-day expiry, 5 failed attempts lock', 'WOULD_FIX');
      END IF;
    ELSE
      log_action('PASSWORD', 'CHECK_PROFILE', 'SECURE_APP_PROFILE', 'Already exists', 'OK');
    END IF;

    -- Apply profile to users with DEFAULT profile
    FOR r IN (SELECT username FROM dba_users
              WHERE profile = 'DEFAULT'
                AND account_status = 'OPEN'
                AND username NOT IN (SELECT TRIM(REGEXP_SUBSTR(g_exclude_users, '[^,]+', 1, LEVEL))
                                     FROM dual
                                     CONNECT BY REGEXP_SUBSTR(g_exclude_users, '[^,]+', 1, LEVEL) IS NOT NULL))
    LOOP
      g_issues_found := g_issues_found + 1;

      IF NOT g_dry_run AND v_profile_exists > 0 THEN
        BEGIN
          EXECUTE IMMEDIATE 'ALTER USER ' || r.username || ' PROFILE secure_app_profile';
          g_issues_fixed := g_issues_fixed + 1;
          log_action('PASSWORD', 'APPLY_PROFILE', r.username, 'Applied secure_app_profile', 'FIXED');
        EXCEPTION
          WHEN OTHERS THEN
            log_action('PASSWORD', 'APPLY_PROFILE', r.username, NULL, 'ERROR', SQLERRM);
        END;
      ELSE
        log_action('PASSWORD', 'APPLY_PROFILE', r.username, 'Would apply secure_app_profile', 'WOULD_FIX');
      END IF;
    END LOOP;
  END enforce_password_policy;

